package com.metawebthree.notification.application.dto;


public class NotificationDTO {
    private Long id;
    private Long userId;
    private String notificationType;
    private String title;
    private String content;
    private String channel;
    private String channelDesc;
    private Integer sendStatus;
    private String sendStatusDesc;
    private Integer readStatus;
    private String readStatusDesc;
    private String sendTime;
    private String readTime;
    private String extraData;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Long getUserId() {
        return userId;
    }

    public void setUserId(Long userId) {
        this.userId = userId;
    }

    public String getNotificationType() {
        return notificationType;
    }

    public void setNotificationType(String notificationType) {
        this.notificationType = notificationType;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getChannel() {
        return channel;
    }

    public void setChannel(String channel) {
        this.channel = channel;
    }

    public String getChannelDesc() {
        return channelDesc;
    }

    public void setChannelDesc(String channelDesc) {
        this.channelDesc = channelDesc;
    }

    public Integer getSendStatus() {
        return sendStatus;
    }

    public void setSendStatus(Integer sendStatus) {
        this.sendStatus = sendStatus;
    }

    public String getSendStatusDesc() {
        return sendStatusDesc;
    }

    public void setSendStatusDesc(String sendStatusDesc) {
        this.sendStatusDesc = sendStatusDesc;
    }

    public Integer getReadStatus() {
        return readStatus;
    }

    public void setReadStatus(Integer readStatus) {
        this.readStatus = readStatus;
    }

    public String getReadStatusDesc() {
        return readStatusDesc;
    }

    public void setReadStatusDesc(String readStatusDesc) {
        this.readStatusDesc = readStatusDesc;
    }

    public String getSendTime() {
        return sendTime;
    }

    public void setSendTime(String sendTime) {
        this.sendTime = sendTime;
    }

    public String getReadTime() {
        return readTime;
    }

    public void setReadTime(String readTime) {
        this.readTime = readTime;
    }

    public String getExtraData() {
        return extraData;
    }

    public void setExtraData(String extraData) {
        this.extraData = extraData;
    }
}